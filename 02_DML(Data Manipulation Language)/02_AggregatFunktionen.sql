/*
+-------------------+---------------------------------------------------------------+
| Schlüsselwort     | Funktionalität                                                |
+-------------------+---------------------------------------------------------------+
| COUNT             | Zählt die Anzahl der Zeilen in einer Ergebnismenge            |
